#include <stdio.h>
#include <unistd.h>

void		ft_print_numbers(void)
{
	int		i;
	char	a;

	a = '0';
	i = 0;
	while (i < 10)
	{
		write(1, &a, 1);
		a++;
		i++;
	}
}
