#include <stdio.h>
#include <unistd.h>

int		ft_is_prime(int nb)
{
	int n;

	n = 2;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (0);
	while (n <= nb / 2)
	{
		if (nb % n == 0)
			return (0);
		n++;
	}
	return (1);
}
